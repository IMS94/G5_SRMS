     * Logs in a student
     * @param Request $request
     * @return \Illuminate\Http\RedirectResponse
     */
    public function login(Request $request){
        Log::info($request->all());
        if(Auth::attempt(['examination_no'=>$request->examination_no,'password'=>$request->password])){
            return redirect()->to('/');
        }
        return back();
    }


